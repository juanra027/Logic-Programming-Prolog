    public void actualizaTabla(ArrayList listaMaterias,JTable Tabla){
        if (listaMaterias!=null&& !listaMaterias.isEmpty()){
            DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
            int cFilas = modelo.getRowCount();
            int cont = 0;
            while(cont != cFilas){
                modelo.removeRow(0); 
                cont+=1;
            }
            Object[] fila = new Object[modelo.getColumnCount()];
            for(int i = 0; i < listaMaterias.size();i++){
                String a = (String) listaMaterias.get(i);
                    fila[0] = false;
                    fila[1] = a;
                modelo.addRow(fila);
            }
        }
        else{
            DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
            int cFilas = modelo.getRowCount();
            int cont = 0;
            while(cont != cFilas){
                modelo.removeRow(0); 
                cont+=1;
            }
        }
           
    }

